package conditionals;
public class relationalOperators {
    public static void main(String[] args) {
        int a = 5, b = 2;
        System.out.println(a==b);
        System.out.println(b==a);
        System.out.println(a!=b);
        System.out.println(a>=b);
        System.out.println(a<=b);

    }
}
