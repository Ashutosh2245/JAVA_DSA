package Basics;
public class variableNamingRules {
    // Variable name starts with alphabets or underscore or dollar sign.
    // Any special character except _ and $ is not allowed.
    // Variable name can not start with digits.
    // Commas and blank spaces are not allowed between variable names.
    // Some particular keywords are not allowed.
    // BASICSALARY _basic minfovermatter FLOAT hELLO -->> These variable names are allowed.

    // basic-hra, #Mean, group., 422, age in 2016, over time, queue.,
    // team's victory, Plot#3, 2015_DDay -->> These variables are not allowed.


}
