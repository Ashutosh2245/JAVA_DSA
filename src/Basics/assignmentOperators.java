package Basics;
public class assignmentOperators {
    public static void main(String[] args) {
        int x = 6;
        x += 5; // x = x + 5
        System.out.println(x);
        x -= 1;
        System.out.println(x);
        x *= 2; // x = x * 2
        System.out.println(x);
        x /= 5;
        System.out.println(x);

    }
}
